package chatMatching.vo;

public class MatchingVo {
	String matchingCode;
	String employerCode;
	String employeeCode;
	String ebCode;
	String title;
	String matchingDate;
	String cfCheck;
	String groupID;
	String reviewCode;
	
	
	public MatchingVo() {};
	
	public String getMatchingCode() {
		return matchingCode;
	}
	public void setMatchingCode(String matchingCode) {
		this.matchingCode = matchingCode;
	}
	public String getEmployerCode() {
		return employerCode;
	}
	public void setEmployerCode(String employerCode) {
		this.employerCode = employerCode;
	}
	public String getEmployeeCode() {
		return employeeCode;
	}
	public void setEmployeeCode(String employeeCode) {
		this.employeeCode = employeeCode;
	}
	public String getEbCode() {
		return ebCode;
	}
	public void setEbCode(String ebCode) {
		this.ebCode = ebCode;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getMatchingDate() {
		return matchingDate;
	}
	public void setMatchingDate(String matchingDate) {
		this.matchingDate = matchingDate;
	}
	public String getCfCheck() {
		return cfCheck;
	}
	public void setCfCheck(String cfCheck) {
		this.cfCheck = cfCheck;
	}
	public String getGroupID() {
		return groupID;
	}
	public void setGroupID(String groupID) {
		this.groupID = groupID;
	}
	
	public String getReviewCode() {
		return reviewCode;
	}

	public void setReviewCode(String reviewCode) {
		this.reviewCode = reviewCode;
	}

	
}
