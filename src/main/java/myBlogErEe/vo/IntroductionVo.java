package myBlogErEe.vo;

public class IntroductionVo {

	String introductionCode;
	String userCode;
	String category;
	String workspace;
	String contents;
	String openCheck;
	
	
	public String getIntroductionCode() {
		return introductionCode;
	}
	public void setIntroductionCode(String introductionCode) {
		this.introductionCode = introductionCode;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getWorkspace() {
		return workspace;
	}
	public void setWorkspace(String workspace) {
		this.workspace = workspace;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getOpenCheck() {
		return openCheck;
	}
	public void setOpenCheck(String openCheck) {
		this.openCheck = openCheck;
	}
	
	
}
